<p class="sr-eyebrow">How it's built inside</p>
<!-- /wp:paragraph -->
<!-- wp:heading -->
<h2 class="wp-block-heading">An architecture built to last<span class="sr-accent-dot">.</span></h2>
<!-- /wp:heading -->
<!-- wp:paragraph {"fontSize":"base"} -->
<p class="has-base-font-size" style="margin-top:16px;font-size:16px;max-width:760px">A layered backend (router → service → repository → model): the business logic lives in the services, never in the controllers or the models. A frontend organised by feature, with a single source of truth for the data. The slow operations — file analysis, PDF generation, email — go through a queue, so the interface stays responsive.</p>
<!-- /wp:paragraph -->
<!-- wp:columns {"style":{"spacing":{"blockGap":{"left":"48px"},"margin":{"top":"24px"}}},"className":"sr-cascade"} -->
<div class="wp-block-columns sr-cascade" style="margin-top:24px"><!-- wp:column {"width":"50%"} -->
<div class="wp-block-column" style="flex-basis:50%"><!-- wp:html -->
<div class="sr-checklist"><div><span class="sr-mono">Backend</span><span>Python 3.12 + FastAPI — typing, documented API, native async</span></div><div><span class="sr-mono">Data</span><span>PostgreSQL 16 — full-text search, JSONB fields, native enums</span></div><div><span class="sr-mono">Queue</span><span>Celery + Redis for background jobs and caching</span></div><div><span class="sr-mono">Auth</span><span>JWT RS256, refresh in an httpOnly cookie, four roles</span></div></div>
<!-- /wp:html --></div>
<!-- /wp:column -->
<!-- wp:column {"width":"50%"} -->
<div class="wp-block-column" style="flex-basis:50%"><!-- wp:html -->
<div class="sr-checklist"><div><span class="sr-mono">Front</span><span>React + Vite + TypeScript, TanStack Router and Query, Tailwind</span></div><div><span class="sr-mono">Files</span><span>Abstracted storage with a type allowlist and path traversal protection</span></div><div><span class="sr-mono">Tests</span><span>pytest on the backend, Vitest and Playwright on the critical flows</span></div><div><span class="sr-mono">Deploy</span><span>Docker Compose, HTTPS with automatic certificates, file backups</span></div></div>
