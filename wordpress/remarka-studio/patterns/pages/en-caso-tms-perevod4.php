<p class="sr-eyebrow">The problem</p>
<!-- /wp:paragraph -->
<!-- wp:heading -->
<h2 class="wp-block-heading">Running an agency on spreadsheets and email doesn't hold up<span class="sr-accent-dot">.</span></h2>
<!-- /wp:heading -->
<!-- wp:paragraph {"fontSize":"base"} -->
<p class="has-base-font-size" style="margin-top:16px;font-size:16px">A translation bureau handles hundreds of orders a month: every order has a client, a language pair, a specialisation, files, a quote, an assigned translator, a deadline and an invoice. Keeping all of that on spreadsheets, chats and email means only one thing: sooner or later something slips through — and what slips is a delivery.</p>
<!-- /wp:paragraph -->
<!-- wp:paragraph {"fontSize":"base"} -->
<p class="has-base-font-size" style="margin-top:12px;font-size:16px">What was needed was a single system, not ten disconnected tools. One place where every order has a status, every client a record, every translator a workload and every job its invoice.</p>
<!-- /wp:paragraph --></div>
<!-- /wp:column -->
<!-- wp:column {"width":"52%"} -->
<div class="wp-block-column" style="flex-basis:52%"><!-- wp:paragraph {"className":"sr-eyebrow"} -->
<p class="sr-eyebrow">Our answer</p>
<!-- /wp:paragraph -->
<!-- wp:heading -->
<h2 class="wp-block-heading">A complete TMS, built by us<span class="sr-accent-dot">.</span></h2>
<!-- /wp:heading -->
<!-- wp:paragraph {"fontSize":"base"} -->
<p class="has-base-font-size" style="margin-top:16px;font-size:16px">We designed and developed a dedicated Translation Management System: a web app where the order, the payment, the assignment, the delivery and the accounting all flow through. It is the group's internal operating system — we use it every day. And this is the same engineering we put into the custom web apps we build for our clients.</p>
<!-- /wp:paragraph -->
<!-- wp:html -->
<div class="sr-checklist" style="margin-top:20px"><div><span class="sr-mono">✓</span><span>A single source of truth: orders, clients, translators, files, payments</span></div><div><span class="sr-mono">✓</span><span>Every action on an order recorded in a chronological log</span></div><div><span class="sr-mono">✓</span><span>Four roles — client, translator, project manager, administrator</span></div><div><span class="sr-mono">✓</span><span>File access only through a protected endpoint, never static in the clear</span></div></div>
