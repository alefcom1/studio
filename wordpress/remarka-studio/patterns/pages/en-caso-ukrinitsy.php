<p class="sr-eyebrow">The problem</p>
<!-- /wp:paragraph -->
<!-- wp:heading -->
<h2 class="wp-block-heading">A small business can't give away 15% of every night<span class="sr-accent-dot">.</span></h2>
<!-- /wp:heading -->
<!-- wp:paragraph {"fontSize":"base"} -->
<p class="has-base-font-size" style="margin-top:16px;font-size:16px">Renting out two or three houses by the sea means living by the season: a few good months to make the whole year add up. And onto those months come the aggregators — they bring guests, but they take their commission on every booking, sometimes with a fixed fee on top. For a big chain that's a rounding error; for a family guest house it's the difference between a good season and a so-so one.</p>
<!-- /wp:paragraph -->
<!-- wp:paragraph {"fontSize":"base"} -->
<p class="has-base-font-size" style="margin-top:12px;font-size:16px">The site he had before was a business card: a few photos, a phone number, and anyone who wanted to book had to call — or, more often, went back to the aggregator. All the work of pulling people to the site ended up handing the booking to someone else.</p>
<!-- /wp:paragraph --></div>
<!-- /wp:column -->
<!-- wp:column {"width":"52%"} -->
<div class="wp-block-column" style="flex-basis:52%"><!-- wp:paragraph {"className":"sr-eyebrow"} -->
<p class="sr-eyebrow">Our answer</p>
<!-- /wp:paragraph -->
<!-- wp:heading -->
<h2 class="wp-block-heading">A site that doesn't just show — it books and it sells<span class="sr-accent-dot">.</span></h2>
<!-- /wp:heading -->
<!-- wp:paragraph {"fontSize":"base"} -->
<p class="has-base-font-size" style="margin-top:16px;font-size:16px">We turned the business card into a tool that works. Two pieces set it apart from any old guest-house site: a <strong>booking module entirely his own</strong>, on his own domain, with no middleman; and an <strong>area guide with an itinerary planner</strong> that turns "just looking" into "I want to come here". The first takes the booking, the second makes people want to book.</p>
<!-- /wp:paragraph -->
<!-- wp:html -->
<div class="sr-checklist" style="margin-top:20px"><div><span class="sr-mono">✓</span><span>Direct booking on the client's own site — no commission, no subscription</span></div><div><span class="sr-mono">✓</span><span>Three houses in one module: dates, guests, per-night price, surcharges</span></div><div><span class="sr-mono">✓</span><span>An area guide that keeps the visitor on the page and wins them over</span></div><div><span class="sr-mono">✓</span><span>Built for the phone first — that's where people searching for the sea come from</span></div></div>
