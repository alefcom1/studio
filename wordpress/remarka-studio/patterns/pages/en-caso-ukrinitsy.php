<h2 class="wp-block-heading">Not "a room", but a holiday already pictured<span class="sr-accent-dot">.</span></h2>
<!-- /wp:heading -->
<!-- wp:paragraph {"fontSize":"base"} -->
<p class="has-base-font-size" style="margin-top:16px;font-size:16px;max-width:820px">People don't book a room: they book a holiday. So, alongside the module, we built an itinerary planner. The visitor says how many days they have (from 1 to 3), who they're travelling with, what pace they want and what interests them — and gets an itinerary tailored to them, with the places and things to do. It's the kind of content guest-house sites almost never have: the sort that keeps you on the page, sets you daydreaming and walks you straight to the booking module.</p>
