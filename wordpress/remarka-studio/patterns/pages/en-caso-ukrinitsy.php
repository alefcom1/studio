<p class="sr-eyebrow">Booking module</p>
<!-- /wp:paragraph -->
<!-- wp:heading -->
<h2 class="wp-block-heading">A booking engine, without paying for a booking engine<span class="sr-accent-dot">.</span></h2>
<!-- /wp:heading -->
<!-- wp:paragraph {"fontSize":"base"} -->
<p class="has-base-font-size" style="margin-top:16px;font-size:16px;max-width:820px">The heart of the project. The visitor picks one of the three houses — Remarka, House 2 or House 3 — sets the check-in and check-out dates and how many they are; the module works out the per-night price, adds the surcharge for the extra guest and shows the total, right there, without ever leaving the site. It does exactly what the paid systems like Bnovo do — but it's our code on the client's site, so there's no subscription and, above all, no commission on each night. What used to go to the aggregator now stays in the owner's pocket.</p>
<!-- /wp:paragraph -->
