<p class="sr-eyebrow">How it's built inside</p>
<!-- /wp:paragraph -->
<!-- wp:heading -->
<h2 class="wp-block-heading">No new app: the same TMS, another way in<span class="sr-accent-dot">.</span></h2>
<!-- /wp:heading -->
<!-- wp:paragraph {"fontSize":"base"} -->
<p class="has-base-font-size" style="margin-top:16px;font-size:16px;max-width:760px">The Mini App is the same frontend as the <a href="/en/case-studies/tms-perevod4/">TMS</a>, with a dedicated way in through Telegram. It reuses the same endpoints and the same role checks as the back office: no new permissions, no shortcuts. Access starts from the data Telegram signs for every user, validated on our backend.</p>
<!-- /wp:paragraph -->
<!-- wp:columns {"style":{"spacing":{"blockGap":{"left":"48px"},"margin":{"top":"24px"}}},"className":"sr-cascade"} -->
