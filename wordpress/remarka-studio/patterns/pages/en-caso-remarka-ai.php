<p class="sr-eyebrow">What we built</p>
<!-- /wp:paragraph -->
<!-- wp:heading -->
<h2 class="wp-block-heading">The features, from the uploaded file to the translated document<span class="sr-accent-dot">.</span></h2>
<!-- /wp:heading -->
<!-- wp:columns {"style":{"spacing":{"blockGap":{"left":"48px","top":"8px"}}},"className":"sr-cascade"} -->
<div class="wp-block-columns sr-cascade"><!-- wp:column {"width":"50%"} -->
<div class="wp-block-column" style="flex-basis:50%"><!-- wp:html -->
<div class="sr-list-rows" style="margin-top:20px"><div><span class="sr-mono">01</span><span><strong>Multi-format upload.</strong> DOCX, XLSX, PDF with text or scanned, TXT and HTML, up to 100 MB per file, with text and structure extraction.</span></div><div><span class="sr-mono">02</span><span><strong>Automatic pipeline.</strong> Every document runs through the states uploading → analyzing → ready, with domain detection before translation even starts.</span></div><div><span class="sr-mono">03</span><span><strong>AI translation engine.</strong> OpenAI GPT models: one model analyzes domain and style, another translates the text split into chunks.</span></div><div><span class="sr-mono">04</span><span><strong>Auto glossary.</strong> Bilingual terms extracted from the document, editable by hand, with TMX export for external CAT tools.</span></div></div>
