<p class="sr-eyebrow">How it works</p>
<!-- /wp:paragraph -->
<!-- wp:heading -->
<h2 class="wp-block-heading">A document's journey, from upload to translated file<span class="sr-accent-dot">.</span></h2>
<!-- /wp:heading -->
<!-- wp:paragraph {"fontSize":"base"} -->
<p class="has-base-font-size" style="margin-top:16px;font-size:16px;max-width:760px">Every step of the journey is a queued job: if one step is slow — the OCR of a PDF, the translation of a long file — it doesn't block the interface, and it can resume from a checkpoint without starting over.</p>
<!-- /wp:paragraph -->
<!-- wp:columns {"style":{"spacing":{"blockGap":{"left":"48px","top":"8px"},"margin":{"top":"24px"}}},"className":"sr-cascade"} -->
<div class="wp-block-columns sr-cascade" style="margin-top:24px"><!-- wp:column {"width":"50%"} -->
<div class="wp-block-column" style="flex-basis:50%"><!-- wp:html -->
<div class="sr-list-rows"><div><span class="sr-mono">01</span><span><strong>File analysis.</strong> Text and structure extracted from the DOCX, the XLSX or the PDF; for scanned PDFs, OCR steps in.</span></div><div><span class="sr-mono">02</span><span><strong>Domain analysis.</strong> An AI model classifies the topic, the technical level and the style of the document.</span></div><div><span class="sr-mono">03</span><span><strong>Glossary and instructions.</strong> Bilingual terms extracted and custom translation instructions generated for that text.</span></div></div>
<!-- /wp:html --></div>
<!-- /wp:column -->
<!-- wp:column {"width":"50%"} -->
<div class="wp-block-column" style="flex-basis:50%"><!-- wp:html -->
<div class="sr-list-rows"><div><span class="sr-mono">04</span><span><strong>Chunked translation.</strong> The text is split into segments and translated in batches, with translation memory acting as a cache and automatic retry on errors.</span></div><div><span class="sr-mono">05</span><span><strong>Quality check.</strong> Missing segments, terminology, numbers and formatting verified before delivery.</span></div><div><span class="sr-mono">06</span><span><strong>File rebuild.</strong> The document is reassembled with its structure, plus the bilingual version and the TMX export.</span></div></div>
<!-- /wp:html --></div>
<!-- /wp:column --></div>
