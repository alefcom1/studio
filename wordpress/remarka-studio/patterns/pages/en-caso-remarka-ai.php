<p class="sr-eyebrow">How it's built inside</p>
<!-- /wp:paragraph -->
<!-- wp:heading -->
<h2 class="wp-block-heading">A queue-based architecture, built for large files<span class="sr-accent-dot">.</span></h2>
<!-- /wp:heading -->
<!-- wp:paragraph {"fontSize":"base"} -->
<p class="has-base-font-size" style="margin-top:16px;font-size:16px;max-width:760px">A modular backend API; the heavy operations — parsing, OCR, translation, QA, export — go through a queue handled by dedicated workers, so the interface stays responsive and every job can resume from a checkpoint. Document conversion and rebuilding live in a separate Python microservice.</p>
<!-- /wp:paragraph -->
<!-- wp:columns {"style":{"spacing":{"blockGap":{"left":"48px"},"margin":{"top":"24px"}}},"className":"sr-cascade"} -->
<div class="wp-block-columns sr-cascade" style="margin-top:24px"><!-- wp:column {"width":"50%"} -->
<div class="wp-block-column" style="flex-basis:50%"><!-- wp:html -->
<div class="sr-checklist"><div><span class="sr-mono">Front</span><span>Next.js 15 and React 19, TypeScript, Tailwind, Framer Motion</span></div><div><span class="sr-mono">Backend</span><span>NestJS on Fastify, Prisma ORM, modular API</span></div><div><span class="sr-mono">Data</span><span>PostgreSQL — documents, jobs, glossaries, translation memory, usage logs</span></div><div><span class="sr-mono">Queue</span><span>BullMQ + Redis, dedicated workers with checkpoints and retry</span></div></div>
<!-- /wp:html --></div>
<!-- /wp:column -->
<!-- wp:column {"width":"50%"} -->
<div class="wp-block-column" style="flex-basis:50%"><!-- wp:html -->
<div class="sr-checklist"><div><span class="sr-mono">AI</span><span>OpenAI GPT models for domain analysis, translation and QA</span></div><div><span class="sr-mono">Files</span><span>S3-compatible storage (MinIO/S3), OCR with Tesseract and PaddleOCR</span></div><div><span class="sr-mono">Auth</span><span>JWT and Google OAuth, roles and plans, Stripe payments</span></div><div><span class="sr-mono">Deploy</span><span>Docker Compose, Nginx, CI/CD with GitHub Actions</span></div></div>
