<p class="sr-eyebrow">Under the hood</p>
<!-- /wp:paragraph -->
<!-- wp:heading -->
<h2 class="wp-block-heading">An architecture built to last, written in English<span class="sr-accent-dot">.</span></h2>
<!-- /wp:heading -->
<!-- wp:paragraph {"fontSize":"base"} -->
<p class="has-base-font-size" style="margin-top:16px;font-size:16px;max-width:760px">A monorepo with several applications and shared libraries, all in TypeScript and all in English, because it was born as an international product. The heavy work — crawling, analysis, export, recrawls — goes through a queue and runs in the background, so the dashboard stays responsive while it works. The interface code is organised by feature, with a single source of truth for the data.</p>
<!-- /wp:paragraph -->
<!-- wp:columns {"style":{"spacing":{"blockGap":{"left":"48px"},"margin":{"top":"24px"}}},"className":"sr-cascade"} -->
<div class="wp-block-columns sr-cascade" style="margin-top:24px"><!-- wp:column {"width":"50%"} -->
