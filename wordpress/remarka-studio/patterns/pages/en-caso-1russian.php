<p class="sr-eyebrow">What we built</p>
<!-- /wp:paragraph -->
<!-- wp:heading -->
<h2 class="wp-block-heading">The features, from crawl to report<span class="sr-accent-dot">.</span></h2>
<!-- /wp:heading -->
<!-- wp:columns {"style":{"spacing":{"blockGap":{"left":"48px","top":"8px"}}},"className":"sr-cascade"} -->
<div class="wp-block-columns sr-cascade"><!-- wp:column {"width":"50%"} -->
<div class="wp-block-column" style="flex-basis:50%"><!-- wp:html -->
<div class="sr-list-rows" style="margin-top:20px"><div><span class="sr-mono">01</span><span><strong>Crawler with no limits.</strong> Crawls the whole site, honouring robots.txt and sitemaps, following redirect chains and response codes; for JavaScript sites, Playwright steps in.</span></div><div><span class="sr-mono">02</span><span><strong>Technical audit.</strong> Eleven rule groups: duplicate title, meta and H1, canonical, hreflang, meta robots, images (alt and weight), schema.org, security (HTTPS, mixed content).</span></div><div><span class="sr-mono">03</span><span><strong>Internal PageRank and orphans.</strong> A graph of internal links, pages with no inbound links, click depth and a health score from 0 to 100.</span></div><div><span class="sr-mono">04</span><span><strong>Real rankings.</strong> Google Search Console and Yandex Webmaster: queries, clicks and true positions, straight from the source and free.</span></div></div>
<!-- /wp:html --></div>
<!-- /wp:column -->
<!-- wp:column {"width":"50%"} -->
<div class="wp-block-column" style="flex-basis:50%"><!-- wp:html -->
<div class="sr-list-rows" style="margin-top:20px"><div><span class="sr-mono">05</span><span><strong>Backlinks and domain authority.</strong> Backlinks from the public Common Crawl index and domain rating via OpenPageRank, with no paid tools.</span></div><div><span class="sr-mono">06</span><span><strong>Core Web Vitals.</strong> Speed and Lighthouse opportunities page by page, through Google PageSpeed Insights.</span></div><div><span class="sr-mono">07</span><span><strong>Recommendations and alerts.</strong> A prioritised list of fixes built from the audit data; alerts by email and Telegram, scheduled recrawls, and a diff between two crawls.</span></div><div><span class="sr-mono">08</span><span><strong>Full export.</strong> CSV, XLSX and HTML reports, with no paywall: the data comes out whole when you need it.</span></div></div>
<!-- /wp:html --></div>
