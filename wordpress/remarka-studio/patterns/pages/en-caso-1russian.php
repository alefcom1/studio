<div class="sr-checklist"><div><span class="sr-mono">Backend</span><span>Node.js + TypeScript, Fastify — REST and WebSocket APIs for real-time crawling</span></div><div><span class="sr-mono">Data</span><span>PostgreSQL 16 with Prisma, 30+ models: projects, crawls, pages, issues, keywords, backlinks</span></div><div><span class="sr-mono">Queue</span><span>Redis + BullMQ for crawls, analysis and export in the background; scheduled recrawls via cron</span></div><div><span class="sr-mono">Auth</span><span>Cookie sessions, magic-link sign-in, password reset</span></div></div>
<!-- /wp:html --></div>
<!-- /wp:column -->
<!-- wp:column {"width":"50%"} -->
<div class="wp-block-column" style="flex-basis:50%"><!-- wp:html -->
<div class="sr-checklist"><div><span class="sr-mono">Front</span><span>Next.js 14 (App Router), Tailwind CSS, Recharts and TanStack Table</span></div><div><span class="sr-mono">Crawler</span><span>undici for HTTP, Playwright for JavaScript sites</span></div><div><span class="sr-mono">External data</span><span>Search Console, PageSpeed, Common Crawl, OpenPageRank and Yandex</span></div><div><span class="sr-mono">Deploy</span><span>Docker Compose — Postgres, Redis, API, worker and web — with automatic HTTPS</span></div></div>
