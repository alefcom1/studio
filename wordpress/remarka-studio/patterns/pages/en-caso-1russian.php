<p class="sr-eyebrow">Two faces of the platform</p>
<!-- /wp:paragraph -->
<!-- wp:heading -->
<h2 class="wp-block-heading">A dashboard for the analyst, a portal for the projects<span class="sr-accent-dot">.</span></h2>
<!-- /wp:heading -->
<!-- wp:columns {"style":{"spacing":{"blockGap":{"left":"48px","top":"8px"}}},"className":"sr-cascade"} -->
<div class="wp-block-columns sr-cascade"><!-- wp:column {"width":"50%"} -->
<div class="wp-block-column" style="flex-basis:50%"><!-- wp:paragraph {"style":{"typography":{"fontSize":"13px","letterSpacing":"0.04em"}},"className":"sr-mono"} -->
<p class="sr-mono" style="font-size:13px;letter-spacing:0.04em">Monitor</p>
<!-- /wp:paragraph -->
<!-- wp:paragraph {"fontSize":"base"} -->
<p class="has-base-font-size" style="margin-top:8px;font-size:16px">The analyst's workstation. Every project has its own crawls, which start in real time and can be watched live as they run. From there you move to pages, issues, internal links, rankings, competitors and performance — with charts that show how they move over time.</p>
<!-- /wp:paragraph --></div>
<!-- /wp:column -->
<!-- wp:column {"width":"50%"} -->
<div class="wp-block-column" style="flex-basis:50%"><!-- wp:paragraph {"style":{"typography":{"fontSize":"13px","letterSpacing":"0.04em"}},"className":"sr-mono"} -->
<p class="sr-mono" style="font-size:13px;letter-spacing:0.04em">Cabinet</p>
<!-- /wp:paragraph -->
<!-- wp:paragraph {"fontSize":"base"} -->
<p class="has-base-font-size" style="margin-top:8px;font-size:16px">The portal for running projects with the client. Each project moves through stages — from brief to design, development, content, review, launch and maintenance — with quotes and invoices, shared files, approvals and tickets. Access via magic link, no passwords to remember.</p>
