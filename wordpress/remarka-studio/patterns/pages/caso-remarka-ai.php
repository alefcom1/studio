<p class="has-base-font-size" style="margin-top:16px;font-size:16px;max-width:760px">Ogni fase del percorso è un lavoro in coda: se un passaggio è lento — l'OCR di un PDF, la traduzione di un file lungo — non blocca l'interfaccia, e può riprendere da un punto di controllo senza ricominciare da capo.</p>
