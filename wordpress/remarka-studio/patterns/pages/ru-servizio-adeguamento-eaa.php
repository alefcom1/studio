<p class="sr-eyebrow">Частые вопросы</p>
<!-- /wp:paragraph -->
<!-- wp:details -->
<details class="wp-block-details"><summary>Кого касается EAA и касается ли он меня, если я не в ЕС?</summary><!-- wp:paragraph -->
<p>European Accessibility Act действует с 28 июня 2025 года и охватывает многие сайты, продающие товары или услуги потребителям в Евросоюзе: интернет-магазины, банки, транспорт, цифровые сервисы. Закон смотрит на рынок, а не на страну регистрации: если ваш сайт обслуживает клиентов в ЕС, он под действие попадает. Освобождены микропредприятия, оказывающие услуги, — меньше 10 человек и менее 2 млн евро годового оборота. Если сомневаетесь, мы проверим ваш случай до подписания договора.</p>
<!-- /wp:paragraph --></details>
<!-- /wp:details -->
<!-- wp:details -->
<details class="wp-block-details"><summary>Какие штрафы предусмотрены?</summary><!-- wp:paragraph -->
<p>Итальянский закон предусматривает штрафы до 5% оборота за несоответствующие услуги — это одна из самых строгих ставок в ЕС. Во Франции уже начались первые иски к крупным онлайн-ритейлерам. Источники открытые: разбор Bird & Bird и центр AccessibleEU Еврокомиссии (ссылки внизу страницы).</p>
<!-- /wp:paragraph --></details>
<!-- /wp:details -->
<!-- wp:details -->
<details class="wp-block-details"><summary>Что такое декларация о доступности?</summary><!-- wp:paragraph -->
<p>Это публичный документ, которого требует закон: сайт заявляет свой уровень соответствия, перечисляет ещё недоступные части и указывает контакт для жалоб. Мы составляем и публикуем его как часть услуги — без декларации сайт не считается соответствующим, даже если технически доступен.</p>
<!-- /wp:paragraph --></details>
<!-- /wp:details -->
<!-- wp:details -->
<details class="wp-block-details"><summary>Достаточно ли автоматической проверки, чтобы пройти по закону?</summary><!-- wp:paragraph -->
<p>Нет, и честнее сказать об этом прямо. Автоматический тест — как наш бесплатный инструмент — ловит около трети критериев WCAG: то, что способна измерить машина. Остальное — навигация с клавиатуры, работа со скринридером, понятность контента — проверяется только вручную. Поэтому ручной аудит — сердце услуги, а не дополнительная опция.</p>
<!-- /wp:paragraph --></details>
