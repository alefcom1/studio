<p class="has-base-font-size" style="margin-top:16px;font-size:16px;max-width:760px">Backend a strati (router → service → repository → model): la logica di business sta nei servizi, mai nei controller o nei modelli. Frontend organizzato per funzionalità, con una fonte di verità sola per i dati. Le operazioni lente — analisi file, generazione PDF, email — passano da una coda, così l'interfaccia resta reattiva.</p>
