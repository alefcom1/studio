<p class="sr-eyebrow">Frequently asked questions</p>
<!-- /wp:paragraph -->
<!-- wp:details -->
<details class="wp-block-details"><summary>Who is required to comply with the EAA? Does my company qualify?</summary><!-- wp:paragraph -->
<p>The European Accessibility Act took effect in Italy on 28 June 2025 and covers many sites selling goods or services to consumers: e-commerce, banks, transport, digital services. Micro-enterprises providing services are exempt — fewer than 10 people and under € 2 million in annual turnover. If in doubt we check your case before signing: if you’re not required to comply, we tell you.</p>
<!-- /wp:paragraph --></details>
<!-- /wp:details -->
<!-- wp:details -->
<details class="wp-block-details"><summary>What fines apply in Italy?</summary><!-- wp:paragraph -->
<p>The Italian decree sets fines of up to 5% of turnover for non-compliant services. In France the first lawsuits against large online retailers have already begun, and enforcement in Italy has just started. The sources are public: the Bird & Bird guide and the European Commission’s AccessibleEU centre (links at the bottom of the page).</p>
<!-- /wp:paragraph --></details>
<!-- /wp:details -->
<!-- wp:details -->
<details class="wp-block-details"><summary>What is an accessibility statement?</summary><!-- wp:paragraph -->
<p>It’s a public document, required by the law, in which the site declares its level of compliance, any parts not yet accessible, and a contact for reporting problems. We write and publish it as part of the service: without a statement a site isn’t compliant, even if technically accessible.</p>
<!-- /wp:paragraph --></details>
<!-- /wp:details -->
<!-- wp:details -->
<details class="wp-block-details"><summary>Is an automated check enough to be compliant?</summary><!-- wp:paragraph -->
<p>No, and it’s fair to say so plainly. An automated test like our free tool catches roughly a third of the WCAG criteria: what a machine can measure. The rest — keyboard navigation, screen-reader experience, content clarity — can only be checked by hand. That’s why the manual audit is the core of this service, not an add-on.</p>
<!-- /wp:paragraph --></details>
